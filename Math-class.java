public class Main{
    public static void main(String[] args){
    int a = 5;
    int b = 7;
    float c = -3.47f;
    
    // Trouver le plus grand nombre entre deux variables
    System.out.println(Math.max(a, b));
    
    // Trouver le plus petit nombre entre deux variables
    System.out.println(Math.min(a, b));
    
    // Trouver la racine carrée d'une valeur
    System.out.println(Math.sqrt(a));
    
    // Trouver la valeur positive absolue d'une valeur
    System.out.println(Math.abs(c));
    
    // Afficher un nombre aléatoire
    System.out.println(Math.random());
    }   
}
